           const personLink = document.createElement("a");
           const personLinkFrame = document.createElement("div");
           const personName = document.createElement("p");

           personImg.src = person.img;

           personLink.appendChild(personImg);

           personLink.classList.add("person-link");
           personLink.href = "#";

           personLinkFrame.appendChild(personLink);
           personLinkFrame.appendChild(personName);

